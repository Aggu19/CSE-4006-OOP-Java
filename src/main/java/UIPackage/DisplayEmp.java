     public void displayEmpDet(String employeeData)
    {
        DefaultTableModel model = (DefaultTableModel) txtEmpDetails.getModel();
        model.setRowCount(0);
        String employeeDataArray[] = employeeData.split("\n");
        for(String emp : employeeDataArray)
        {
            System.out.println(emp);
            Object[] rowData = {emp.split("__")[0], emp.split("__")[1], 
                                emp.split("__")[2], emp.split("__")[3]};
            
            System.out.println(emp.split("__")[0]+ emp.split("__")[1]+ 
                               emp.split("__")[2]+ emp.split("__")[3]);
            model.addRow(rowData);
        }
    }

