    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
       
        String userName = txtUserName.getText();
        String Password = txtPassword.getText();
        String confirmPassword = txtConfirmPassword.getText();
        String HRType = cmbHRType.getSelectedItem().toString();
        if(userName.equals("") || Password.equals("") || confirmPassword.equals("")){
            JOptionPane.showMessageDialog(null, "Fields cannot be empty!","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if(!(Password.equals(confirmPassword))){
            JOptionPane.showMessageDialog(null, "Password Mismatch!","Error",JOptionPane.ERROR_MESSAGE);
        }
        else if (Password.length()<3){
            JOptionPane.showMessageDialog(null, "Password Too Short!","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
            String record  = HRType + "---" + userName + "---" + Password;
            try{
                FileWriter fileWriter = new FileWriter(myFile,true); //use true to avoid overWrite
                BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);

                bufferedWriter.write(record);
                bufferedWriter.newLine();
                bufferedWriter.close();
                fileWriter.close();

                JOptionPane.showMessageDialog(null, "Successfully wrote the data");
            }
            catch(IOException ex){
                System.out.println("Error occured");
            }
        }

    }//GEN-LAST:event_btnSignupActionPerformed


